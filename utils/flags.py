    # Split input string into individual flags
    input_flag_list = input_flags.split()
    
    # Filter flags to only include valid ones
    valid_flags = []
    for flag in input_flag_list:
        is_valid = False
        for v8_flag in v8_flags:
            if flag == v8_flag or flag.startswith(v8_flag + "="):
                is_valid = True
                break
                
        if is_valid:
            valid_flags.append(flag)
        else:
            red(f"[WARNING] Removing invalid flag: \"{flag}\". If you feel this is wrong, please add your flag to flags.py.")
    
    # Join the valid flags back into a single string
    return " ".join(valid_flags)